# NOTE: Rules for defining a variable name
'''
1. A variable can contain alphabets, digits, and underscores.
2. A variable name can only start with an alphabet and underscores.
3. A variable name can't start with a digit.
4. No whitespace is allowed to be used inside a variable name.
Examples of a few variable names are: keshav, one8, seven, _seven, etc.
'''